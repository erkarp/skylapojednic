			<div><p><b>Cisgender Study – 2012 – Civic Square Building, New Brunswick, NJ </b></p>
				<p>The labeling which has dictated so much of our gendered lives is brought into examination and critiqued as viewers observe life size portraits of a homosexual cisman and a transitioning transwoman posed nude, and clothed, outside of the men’s and women’s bathrooms on the third floor of the Civic Square Building in New Brunswick, NJ.</p>
				<p>The placement of the portraits outside of the bathrooms is meant to educate people of the oftentimes ambiguous state that bodies are left in during the transition period, as well as the effect that clothing and general appearance have on a person’s bathroom access, and the complications that can arise when entering a gendered space.</p>
				<p>Placing the portraits in a bathroom corridor allows the images to impact a viewer’s sense of illusion, and association of gender, and gendered spaces. The intention is to entice viewers to read about the subjects, and to ultimately reconsider the use of gendered labels; hopefully this leads them to consider and sympathize with how (and why) individuals might choose to label themselves in order to safely enter the spaces they do, with comfort, and without ridicule.</p>
